
public class PrimitiveVariables {

	public static void main(String[] args) {
		//variable1
		int year = 12;
		double hours = 97876.954;
		char category = 'A';
		boolean isNumberOne = false;
		String nameSite = "Netflix";
		
		System.out.println(year);
		System.out.println(hours);
		System.out.println(category);
		System.out.println(isNumberOne);
		System.out.println(nameSite);
		
		//variable2
		int dogsAge = 7;
		double weight = 78.35;
		char breedClass = 'K';
		boolean isHeMean = false;
		String dogsName = "Rocco";
		
		System.out.println(dogsAge);
		System.out.println(weight);
		System.out.println(breedClass);
		System.out.println(isHeMean);
		System.out.println(dogsName);
		

	}

}
